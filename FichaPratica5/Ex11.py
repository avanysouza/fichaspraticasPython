#  Implemente um método vogal( ) que aceita uma String como parâmetro e retorna true se for uma vogal ou falso
# caso contrário. Deve tratar devidamente as minúsculas/maiúsculas.


def vogal(letra):
    if letra == "a" or letra == "e" or letra == "o" or letra == "u":
        return True
    else:
        return False



vogal("f")



